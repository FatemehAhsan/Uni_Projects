void lcd_clear_and_set_cursor(void){
	LCD_Clear();
	LCD_Set_Cursor(1, 1);
}
int get_number_length(int x){
	int cnt = 0;
	int tmp = x;
	do{
		tmp = tmp / 10;
		cnt++;
	}while (tmp != 0);
	return cnt;
}
void print_number_on_LCD(int x){
	int cnt = get_number_length(x);
	while(cnt > 0){ 
		int z = x / pow(10, cnt - 1);
		x = x % (int)pow(10, cnt - 1);
		LCD_Write_Char(z + '0');
		cnt = cnt - 1;
	}
}
void print2Zeros(int x){
	if(x < 10)
		LCD_Write_Char('0');
}
void print3Zeros(int x){
	if(x < 100)
		LCD_Write_Char('0');
	if(x < 10)
		LCD_Write_Char('0');
}
void print_time(void){
	lcd_clear_and_set_cursor();
	print2Zeros(MM);
	print_number_on_LCD(MM);
	LCD_Write_Char(':');
	print2Zeros(SS);
	print_number_on_LCD(SS);
	LCD_Write_Char(':');
	print3Zeros(mmm);
	print_number_on_LCD(mmm);
}
void set_time_zero(void){
	mmm = 0;
	SS = 0;
	MM = 0;
	print_time();
}
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if ( GPIO_Pin == GPIO_PIN_0 && turendOff == 0){
		print_time();
		HAL_TIM_Base_Start_IT(&htim2);
	}
	else if ( GPIO_Pin == GPIO_PIN_1 && turendOff == 0)
		HAL_TIM_Base_Stop_IT(&htim2); 
	else if ( GPIO_Pin == GPIO_PIN_2 && turendOff == 0){
		if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_2) == GPIO_PIN_RESET){
			HAL_TIM_Base_Stop_IT(&htim2); 
			set_time_zero();
			HAL_TIM_Base_Start_IT(&htim3);
		}
		else
			HAL_TIM_Base_Stop_IT(&htim3); 
	}
		
}
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	
	if (htim->Instance == TIM2){
		mmm_250++;
		if(mmm_250 == 4){
			mmm_250 = 0;
			SS++;
		}
		mmm = mmm_250 * 250;
		print_time();
		if(SS == 59){
			SS = 0;
			MM++;
		}
	}
	else if (htim->Instance == TIM3){
		tim3_mmm_250++;
		if(turendOff){
			if(tim3_mmm_250 == 2){
				lcd_clear_and_set_cursor();
        LCD_Write_String("Turn Off");
				tim3_mmm_250 = 0;
			}
		}
		else{
			if(tim3_mmm_250 == 12){
				lcd_clear_and_set_cursor();
				LCD_Write_String("Turn Off");
				__HAL_TIM_SET_COUNTER(&htim3, 0); 
				turendOff = 1;
				tim3_mmm_250 = 0;
			}
		}
	}
}
